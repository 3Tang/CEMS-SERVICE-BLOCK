public class UDPResponse {

	private UDPHead head;
	private String json;

	public UDPHead getHead() {
		return head;
	}

	public void setHead(UDPHead head) {
		this.head = head;
	}

	public String getJson() {
		return json;
	}

	public void setJson(String json) {
		this.json = json;
	}

	@Override
	public String toString() {
		return "UDPResponse [head=" + head + ", json=" + json + "]";
	}
	
}
